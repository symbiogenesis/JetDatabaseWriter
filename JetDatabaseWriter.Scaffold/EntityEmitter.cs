namespace JetDatabaseWriter.Scaffold;

using System;
using System.Collections.Frozen;
using System.Collections.Generic;
using System.Globalization;

using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;

using static Microsoft.CodeAnalysis.CSharp.SyntaxFactory;

/// <summary>
/// Generates C# source code for an entity model from column metadata using Roslyn.
/// </summary>
internal static class EntityEmitter
{
    private static readonly FrozenDictionary<Type, string> FriendlyTypeNames = new Dictionary<Type, string>
    {
        [typeof(bool)] = "bool",
        [typeof(byte)] = "byte",
        [typeof(short)] = "short",
        [typeof(int)] = "int",
        [typeof(long)] = "long",
        [typeof(float)] = "float",
        [typeof(double)] = "double",
        [typeof(decimal)] = "decimal",
        [typeof(string)] = "string",
        [typeof(DateTime)] = "DateTime",
        [typeof(DateTimeOffset)] = "DateTimeOffset",
        [typeof(TimeSpan)] = "TimeSpan",
        [typeof(Guid)] = "Guid",
        [typeof(byte[])] = "byte[]",
        [typeof(object)] = "object",
    }.ToFrozenDictionary();

    /// <summary>
    /// Emits a complete C# source file for a single entity class or record.
    /// </summary>
    public static string Emit(
        string className,
        List<ColumnMetadata> columns,
        string ns,
        bool useRecords,
        bool nullable)
    {
        // Build the type declaration (class or record)
        TypeDeclarationSyntax typeDecl = useRecords
            ? RecordDeclaration(Token(SyntaxKind.RecordKeyword), className)
                .WithOpenBraceToken(Token(SyntaxKind.OpenBraceToken))
                .WithCloseBraceToken(Token(SyntaxKind.CloseBraceToken))
            : ClassDeclaration(className);

        typeDecl = typeDecl.AddModifiers(
            Token(SyntaxKind.PublicKeyword),
            Token(SyntaxKind.SealedKeyword));

        var usedNames = new HashSet<string>(StringComparer.Ordinal);

        foreach (var col in columns)
        {
            string propName = NameCleaner.ToPropertyName(col.Name);

            // Deduplicate property names within the same class
            string baseName = propName;
            int suffix = 2;
            while (!usedNames.Add(propName))
            {
                propName = baseName + suffix.ToString(CultureInfo.InvariantCulture);
                suffix++;
            }

            // Avoid property name colliding with class name
            if (propName == className)
            {
                propName += "Value";
                usedNames.Add(propName);
            }

            TypeSyntax typeSyntax = ParseTypeName(MapClrType(col.ClrType, col.IsNullable, nullable));

            var property = PropertyDeclaration(typeSyntax, propName)
                .AddModifiers(Token(SyntaxKind.PublicKeyword))
                .AddAccessorListAccessors(
                    AccessorDeclaration(SyntaxKind.GetAccessorDeclaration)
                        .WithSemicolonToken(Token(SyntaxKind.SemicolonToken)),
                    AccessorDeclaration(SyntaxKind.SetAccessorDeclaration)
                        .WithSemicolonToken(Token(SyntaxKind.SemicolonToken)));

            // Add default value initializer when needed
            ExpressionSyntax? initializer = DefaultValueExpression(col.ClrType, col.IsNullable, nullable);
            if (initializer is not null)
            {
                property = property
                    .WithInitializer(EqualsValueClause(initializer))
                    .WithSemicolonToken(Token(SyntaxKind.SemicolonToken));
            }

            // Add XML doc comment
            string summary = $"Column: {EscapeXml(col.Name)} ({col.TypeName}, {col.Size}).";
            var docComment = XmlDocSummary(summary);
            property = property.WithLeadingTrivia(
                Trivia(docComment),
                ElasticLineFeed);

            typeDecl = typeDecl.AddMembers(property);
        }

        // Build the namespace
        var nsDecl = FileScopedNamespaceDeclaration(ParseName(ns))
            .AddUsings(UsingDirective(ParseName("System")))
            .AddMembers(typeDecl);

        // Build the compilation unit
        var compilationUnit = CompilationUnit()
            .AddMembers(nsDecl);

        var leadingTrivia = new List<SyntaxTrivia>
        {
            Comment("// <auto-generated>"),
            ElasticLineFeed,
        };

        if (nullable)
        {
            leadingTrivia.Add(Trivia(NullableDirectiveTrivia(Token(SyntaxKind.EnableKeyword), isActive: true)));
            leadingTrivia.Add(ElasticLineFeed);
        }

        compilationUnit = compilationUnit.WithLeadingTrivia(leadingTrivia);

        var source = compilationUnit
            .NormalizeWhitespace()
            .ToFullString();

        const string eol = "\r\n";

        // Insert blank line before namespace declaration
        source = source.Replace(eol + "namespace ", eol + eol + "namespace ", StringComparison.Ordinal);

        // Insert blank line between file-scoped namespace and first using directive
        int usingIdx = source.IndexOf(";" + eol + "using ", StringComparison.Ordinal);
        if (usingIdx >= 0)
        {
            source = source.Insert(usingIdx + 1 + eol.Length, eol);
        }

        // Insert blank lines between properties (before each /// except the first)
        source = source.Replace(eol + "    /// <summary>", eol + eol + "    /// <summary>", StringComparison.Ordinal);

        // Remove the unwanted blank line before the very first property (after open brace)
        source = source.Replace("{" + eol + eol + "    /// <summary>", "{" + eol + "    /// <summary>", StringComparison.Ordinal);

        return source;
    }

    private static string MapClrType(Type clrType, bool isNullable, bool nullableEnabled)
    {
        string baseName = GetFriendlyTypeName(clrType);

        return isNullable && (nullableEnabled || clrType.IsValueType)
            ? baseName + "?"
            : baseName;
    }

    private static string GetFriendlyTypeName(Type type)
    {
        if (FriendlyTypeNames.TryGetValue(type, out string? name))
        {
            return name;
        }

        // Handle Nullable<T> that might already be in the metadata
        Type? underlying = Nullable.GetUnderlyingType(type);
        return underlying is not null
            ? GetFriendlyTypeName(underlying) + "?"
            : type.Name;
    }

    private static ExpressionSyntax? DefaultValueExpression(Type clrType, bool isNullable, bool nullableEnabled)
    {
        if (isNullable)
        {
            return null;
        }

        if (nullableEnabled && !clrType.IsValueType)
        {
            if (clrType == typeof(string))
            {
                return MemberAccessExpression(
                    SyntaxKind.SimpleMemberAccessExpression,
                    PredefinedType(Token(SyntaxKind.StringKeyword)),
                    IdentifierName("Empty"));
            }

            if (clrType == typeof(byte[]))
            {
                return InvocationExpression(
                    MemberAccessExpression(
                        SyntaxKind.SimpleMemberAccessExpression,
                        IdentifierName("Array"),
                        GenericName(Identifier("Empty"))
                            .WithTypeArgumentList(
                                TypeArgumentList(
                                    SingletonSeparatedList<TypeSyntax>(
                                        PredefinedType(Token(SyntaxKind.ByteKeyword)))))));
            }

            return PostfixUnaryExpression(
                SyntaxKind.SuppressNullableWarningExpression,
                LiteralExpression(SyntaxKind.DefaultLiteralExpression));
        }

        return null;
    }

    private static DocumentationCommentTriviaSyntax XmlDocSummary(string text)
    {
        return DocumentationCommentTrivia(
            SyntaxKind.SingleLineDocumentationCommentTrivia,
            List(new XmlNodeSyntax[]
            {
                XmlText()
                    .WithTextTokens(
                        TokenList(
                            XmlTextLiteral(
                                TriviaList(DocumentationCommentExterior("///")),
                                " ",
                                " ",
                                TriviaList()))),
                XmlElement(
                    XmlElementStartTag(XmlName("summary")),
                    SingletonList<XmlNodeSyntax>(XmlText(text)),
                    XmlElementEndTag(XmlName("summary"))),
                XmlText()
                    .WithTextTokens(
                        TokenList(
                            XmlTextNewLine(
                                TriviaList(),
                                "\n",
                                "\n",
                                TriviaList()))),
            }));
    }

    private static string EscapeXml(string text)
    {
        return text
            .Replace("&", "&amp;", StringComparison.Ordinal)
            .Replace("<", "&lt;", StringComparison.Ordinal)
            .Replace(">", "&gt;", StringComparison.Ordinal)
            .Replace("\"", "&quot;", StringComparison.Ordinal);
    }
}
